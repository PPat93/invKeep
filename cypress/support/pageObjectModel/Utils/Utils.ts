/**
 * 
 * Utility class containing all methods and variables used across diferent invKeep pages
 * @class
 * 
 */
class Utils{

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //  SELECTORS                   ////////////////////////////////////////////////////////////////////////////////////////////////////////
    readonly createAssetBtn = `create-asset`;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //  METHODS                     ////////////////////////////////////////////////////////////////////////////////////////////////////////
    visitPage(pageUrl: string) {

        cy.visit(pageUrl);
        cy.url()
            .should(`contain`, pageUrl);
    }
}

export default new Utils();
